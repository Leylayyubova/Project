function filterPartners() {
  const categorySelect = document.querySelector("#category");
  const partnerListElement = document.querySelector(".cards");


  let filteredPartners = partners;

  categorySelect.addEventListener("change", function (e) {
    const selectedCategory = e.target.value.toLowerCase();

    if (selectedCategory !== "bütün") {

      filteredPartners = partners.filter(partner => partner.category.toLowerCase() === selectedCategory);
    } else {

      filteredPartners = partners;
    }

    updatePartnerList();
  });

  updatePartnerList();

  function updatePartnerList() {
    let partnerCard = "";
    filteredPartners.forEach(partner => {
      partnerCard += `
              <div class="card">   
              <div class="inner">           
                    <div class="img">
                    <img src="${partner.imgUrl}" alt="${partner.title}">
                    </div>
                    <h3>${partner.title}</h3>
                    <p>${partner.category}</p>   
                    <div class="month">
    ${partner.kredit.map(credit => `<p class="credit-option">${credit}<span class="zero-percent">0%</span></p> `).join('')}
    </div>
    ${partner.cashback ? `<p class="cash">${partner.cashback}</p>` : ''}          
              </div>
              </div> 
          `;
    });
    partnerListElement.innerHTML = partnerCard;
  }
}

filterPartners();


function searchFunction() {
  let input = document.querySelector('input[type="text"]').value.toLowerCase();
  let filteredPartners = partners.filter(partner => partner.title.toLowerCase().includes(input));

  let cardsContainer = document.querySelector('.cards');
  cardsContainer.innerHTML = "";

  filteredPartners.forEach(partner => {
    let partnerCard = document.createElement("div");
    partnerCard.classList.add("partner-card");
    partnerCard.innerHTML = `
      <div class="card">
    <div class="inner">
    <div class="img">
    <img src="${partner.imgUrl}" alt="${partner.title}">
    </div>
    <h3>${partner.title}</h3>
    <p>${partner.category}</p>
    <div class="month">
    ${partner.kredit.map(credit => `<p class="credit-option">${credit}<span class="zero-percent">0%</span></p> `).join('')}
    </div>
    ${partner.cashback ? `<p class="cash">${partner.cashback}</p>` : ''}
    </div>
      </div>
      `;
    cardsContainer.appendChild(partnerCard);
  });
  cardsContainer.style.display = 'flex';
  cardsContainer.style.flexWrap = 'wrap';
}

searchFunction();
